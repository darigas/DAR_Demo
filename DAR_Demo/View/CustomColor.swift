import UIKit

class CustomColor: UIColor {
    static var violetLight: UIColor {
        return UIColor(red: 100/255, green: 100/255, blue: 255/255, alpha: 1.5)
    }
    
    static var violetDark: UIColor {
        return UIColor(red: 17/255, green: 17/255, blue: 42/255, alpha: 1.0)
    }
    
    static var sunset: UIColor {
        return UIColor(red: 255/255, green: 100/255, blue: 100/255, alpha: 1.0)
    }
}
